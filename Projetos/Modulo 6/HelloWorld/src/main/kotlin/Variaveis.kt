/**
 * Variáveis armazenam valores qeu podem ser usados posteriormente dentro de um programa.
 * Toda variável possui um tipo. É o tipo de dado que diz qual o valor que pode ser armazenado.
 * Toda variável possui um nome, e com ela é referenciada dentro do programa.
 *
 * Nenhuma variável pode ser declarada com uma palavra reservada, caracteres especiais ou espaços.
 * Palavras reservadas são palavras usadas para a linguagem, como fun, var, val, return, while, etc.
 */
fun main() {
    // Variável do tipo String explicitamente declarada
    var nome: String = "Jane"

    // Usando variável nome
    println("Olá, $nome")
}